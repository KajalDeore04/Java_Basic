public class CWH_operators {

    public static void main(String[] args) {
        int a=8;
    //    int b=a%2;     //modulus shows reminder of the division
        int b=9;
    //    b+=3;            // 3 added to the 9
        b*=3;               // 9 multiplied by 3


        System.out.println(b);
        System.out.println(b> 8);                 //showed than answer is not equal to 8,same for greater than
        System.out.println(64>5 && 64>100);        //true if both conditions satisfied
        System.out.println(64>5 ||  64>100);        //shows true even if one condition is satisfied
        System.out.println(2&3);                    //bitwise operator
                                                     //10
                                                     //11
                                                //----------
                                                    //10->2 in bit




    }
}
